## 運算子 與 運算元
print("2 + 3 = ", 2 + 3) # addition (+) 加
print("3 - 1 = ", 3 - 1) # subtraction (-) 減
print("2 * 3 = ", 2 * 3) # multiplication (*) 乘
print("3 / 2 = ", 3 / 2) # division (/) 除
print("3 ^ 2 = ", 3 ** 2)# exponential ( ** )
print("3 % 2 = ", 3 % 2) # modulus(%)
print(3 // 2)  # Floor division operator(//)

## Checking data types

print(type(10))                   # Int
print(type(3.14))                 # Float
print(type(1 + 3j))               # Complex
print(type('Asabeneh'))           # String
print(type([1, 2, 3]))            # List []
print(type({'name':'Asabench'}))  # Dictionary {key:value}
print(type({9.8, 3.14, 2.7}))     # Set {}
print(type((9.8, 3.14, 2.7)))     # Tuple ()







